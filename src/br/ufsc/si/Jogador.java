package br.ufsc.si;

public class Jogador {

	private String peca;
	private JogadorTipo tipo;

	public String getPeca() {
		return peca;
	}

	public void setPeca(String peca) {
		this.peca = peca;
	}

	public JogadorTipo getTipo() {
		return tipo;
	}

	public void setTipo(JogadorTipo tipo) {
		this.tipo = tipo;
	}

}
